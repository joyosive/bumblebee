/**
 * Fuzzy search across ventures by name, sector, country, description.
 */
export function searchVentures(query: string): Venture[] {
  const lower = query.toLowerCase();
  const terms = lower.split(/\s+/).filter(t => t.length > 2);

  return VENTURES.filter(v => {
    const searchable = `${v.name} ${v.sector} ${v.countries} ${v.description} ${v.communitiesServed}`.toLowerCase();
    return terms.some(term => searchable.includes(term));
  }).sort((a, b) => {
    // Prioritize name matches
    const aName = terms.some(t => a.name.toLowerCase().includes(t)) ? 0 : 1;
    const bName = terms.some(t => b.name.toLowerCase().includes(t)) ? 0 : 1;
    return aName - bName;
  });
}

/**
 * Find a venture by ID or name (case-insensitive partial match).
 * Strips common command words so "Verify Acceso" finds "Acceso".
 */
export function findVenture(query: string): Venture | undefined {
  const lower = query.toLowerCase();

  // Direct match first
  const direct = VENTURES.find(v => v.id === lower || v.name.toLowerCase() === lower)
    || VENTURES.find(v => v.name.toLowerCase().includes(lower));
  if (direct) return direct;

  // Strip command words and try again
  const stripped = lower
    .replace(/\b(verify|check|analyze|fund|release|finish|escrow|donate|find|search)\b/g, '')
    .replace(/\d+\s*xrp/g, '')
    .replace(/\bwith\b/g, '')
    .replace(/\bfor\b/g, '')
    .trim();

  if (!stripped) return undefined;

  return VENTURES.find(v => v.id === stripped || v.name.toLowerCase() === stripped)
    || VENTURES.find(v => v.name.toLowerCase().includes(stripped))
    || VENTURES.find(v => stripped.includes(v.name.toLowerCase()));
}
